public class P8메소드 {
    public static void main(String[] args) {
        //메소드 그림..
        int sum=sum(3,5);
        System.out.println(sum(3,5));
        printNewjeansMember();
    }

    //메소드는 리턴타입 이름(파라미터) {실행문으로 구성}
    public static int sum(int a, int b){
        return a+b;
    }
    public static void printNewjeansMember(){
        System.out.println("민지 하니 다니엘 해린 혜인");
    }


}
